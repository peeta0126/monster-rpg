/**
 * 몬스터 픽셀아트 드로우 함수 모음
 *
 * 각 함수는 Phaser.GameObjects.Graphics 객체와 픽셀 단위 크기(ps)를 받아
 * 80×80(ps=4 기준) 캔버스에 몬스터를 그린다.
 *
 * 좌표 기준: 왼쪽 상단 (0,0), 중심 근처 (cx=ps*10, cy=ps*10)
 */

type DrawFn = (g: Phaser.GameObjects.Graphics, ps: number) => void;

// ─── 공통 헬퍼 ──────────────────────────────────────────────────────────────────

/** 단일 "픽셀" (ps×ps 사각형) 채우기 */
function px(
  g: Phaser.GameObjects.Graphics,
  col: number,
  row: number,
  ps: number,
  color: number,
  alpha = 1
) {
  g.fillStyle(color, alpha);
  g.fillRect(col * ps, row * ps, ps, ps);
}

